const { default: BinaryStream } = require('../dist/index.js')

const stream = new BinaryStream()

stream.writeUInt8(255)
stream.writeUInt16(65535)
stream.writeUInt32(4294967295)
stream.writeInt8(-128)
stream.writeInt16(-32768)
stream.writeInt32(-2147483648)
stream.writeUInt24LE(16777215)
stream.writeUInt24BE(16777215)
stream.writeInt24LE(-8388608)
stream.writeInt24BE(-8388608)

console.log(stream.readUInt8())
console.log(stream.readUInt16())
console.log(stream.readUInt32())
console.log(stream.readInt8())
console.log(stream.readInt16())
console.log(stream.readInt32())
console.log(stream.readUInt24LE())
console.log(stream.readUInt24BE())
console.log(stream.readInt24LE())
console.log(stream.readInt24BE())
